package com.company;

public class Employee {
    int empid;
    String name;
    String role;
    int experience;
    String department;
    int salary;
   public Employee(int id , String name, String role , int experience , String department, int salary){
       empid = id;
       this.name = name;
       this.role = role;
       this.experience = experience;
       this.department = department;
       this.salary = salary;
   }

}
